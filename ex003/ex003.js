var n2 = prompt("Digite outro número");
n2 = parseInt(n2);

alert("O Resultado da substração é:  " + (n1 - n2));
alert("O Resultado da multiplicação é:  " + (n1 * n2));
alert("O Resultado da divisão é:  " + (n1 / n2));
